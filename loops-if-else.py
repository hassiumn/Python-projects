# 1. Перевірка віку:
# Запитай у користувача вік. Якщо йому 18 або більше — виведи "Повнолітній", інакше — "Неповнолітній".

# 2. Парне чи непарне:
# Запитай число, визначи, чи воно парне (ділиться на 2 без остачі).

# 3. Оцінка по балу:
# Запитай число (0–100). Якщо >= 90 — "Відмінно", 70–89 — "Добре", менше — "Слабко".

# 4. Вгадай число:
# В програмі є число (наприклад 7). Користувач вводить своє. Програма відповідає — вгадав чи ні.

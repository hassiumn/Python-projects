# -----------------------Solutions--------------------


#task 1
age = int(input('Enter ur age:'))
if age >= 18 :
    print ('Повнолітній')
else:
    print ('Неповнолітній')

#task 2
num = int(input('Enter a number:'))
if num % 2 == 0:
    print ('Парне')
else:
    print ('Непарне')

#task 3
score = int(input('Enter a number (0-100):'))
if score >= 90:
    print ('Відмінно')
elif score >= 70 and score < 90:
    print ('Добре')
else:
    print ('Слабко')

#task 4
number = range(1, 10)
user_number = int(input('Enter a number: (1-10):'))
